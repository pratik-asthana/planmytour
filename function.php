function getFlightTime($departureDatetime){
    $arr = explode("T",$departureDatetime);
    
    $time = explode(':',$arr[1]);
    unset($time[2]);
    
    $originalDate = $arr[0];
    $newDate = date("d/m/Y", strtotime($originalDate));
    
    return $newDate.','.implode(":",$time);
}


